"use client"

import { useState } from "react"
import { useParams } from "next/navigation"
import { useQuery } from "@tanstack/react-query"
import { createBrowserClient } from "@/lib/supabase/client"
import { Download, Printer, AlertCircle } from "lucide-react"
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from "@/components/ui/card"
import { Button } from "@/components/ui/button"

export default function PredictionReportPage() {
  const params = useParams()
  const predictionId = params.id as string
  const supabase = createBrowserClient()
  const [generatingReport, setGeneratingReport] = useState(false)

  // Fetch prediction data
  const { data: prediction } = useQuery({
    queryKey: ["prediction", predictionId],
    queryFn: async () => {
      const { data } = await supabase.from("health_risk_predictions").select("*").eq("id", predictionId).single()
      return data
    },
  })

  async function generateFullReport() {
    setGeneratingReport(true)
    try {
      const response = await fetch("/api/admin/generate-diagnosis-report", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          patientId: prediction?.patient_id,
          predictionId,
          doctorId: prediction?.doctor_id,
        }),
      })

      const { report } = await response.json()

      // Open in new window for printing
      const newWindow = window.open()
      if (newWindow) {
        newWindow.document.write(report)
        newWindow.document.close()
      }
    } catch (error) {
      console.error("Error generating report:", error)
    } finally {
      setGeneratingReport(false)
    }
  }

  function downloadReportAsPDF() {
    // In production, use a PDF library like jsPDF
    alert("PDF download feature would require jsPDF library - contact administrator")
  }

  return (
    <main className="min-h-screen bg-background">
      <div className="border-b border-border bg-card">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
          <h1 className="text-3xl font-bold text-foreground">Generate Diagnosis Report</h1>
          <p className="mt-2 text-sm text-muted-foreground">
            Create comprehensive diagnosis report for medical records
          </p>
        </div>
      </div>

      <div className="max-w-3xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
        <Card className="mb-6 border-destructive/20 bg-destructive/5">
          <CardContent className="pt-6">
            <div className="flex gap-3">
              <AlertCircle className="h-5 w-5 text-destructive flex-shrink-0" />
              <div className="text-sm">
                <p className="font-semibold text-foreground">Medical Disclaimer</p>
                <p className="text-muted-foreground mt-1">
                  This report is generated by AI for research purposes. It must be reviewed and validated by licensed
                  medical professionals before clinical use. Not for diagnosis.
                </p>
              </div>
            </div>
          </CardContent>
        </Card>

        <div className="space-y-6">
          <Card>
            <CardHeader>
              <CardTitle>Report Generation Options</CardTitle>
              <CardDescription>Select format and generate comprehensive diagnosis report</CardDescription>
            </CardHeader>
            <CardContent className="space-y-4">
              {prediction && (
                <div className="p-4 bg-muted rounded-lg space-y-2">
                  <p className="font-medium text-foreground">Prediction Summary:</p>
                  <p className="text-sm text-muted-foreground">
                    • Disease: <strong>{prediction.disease_name}</strong>
                  </p>
                  <p className="text-sm text-muted-foreground">
                    • Confidence: <strong>{(prediction.confidence * 100).toFixed(1)}%</strong>
                  </p>
                  <p className="text-sm text-muted-foreground">
                    • Risk Score: <strong>{prediction.risk_score?.toFixed(1) || "N/A"}</strong>
                  </p>
                </div>
              )}

              <div className="space-y-2">
                <p className="font-medium text-foreground text-sm">Choose Report Format:</p>

                <Button
                  onClick={generateFullReport}
                  disabled={generatingReport || !prediction}
                  className="w-full justify-start bg-transparent"
                  variant="outline"
                >
                  <AlertCircle className="mr-2 h-4 w-4" />
                  View Full Report (HTML Format)
                </Button>

                <Button
                  onClick={() => {
                    if (prediction) {
                      const text = `PREDICTION REPORT\nDisease: ${prediction.disease_name}\nConfidence: ${(prediction.confidence * 100).toFixed(1)}%\nRisk Score: ${prediction.risk_score?.toFixed(1) || "N/A"}\nICD-10: ${prediction.icd10_code}\n\nExplanation:\n${prediction.explanation}`
                      const element = document.createElement("a")
                      element.setAttribute("href", "data:text/plain;charset=utf-8," + encodeURIComponent(text))
                      element.setAttribute("download", `diagnosis_report_${predictionId}.txt`)
                      element.style.display = "none"
                      document.body.appendChild(element)
                      element.click()
                      document.body.removeChild(element)
                    }
                  }}
                  disabled={!prediction}
                  className="w-full justify-start"
                  variant="outline"
                >
                  <Download className="mr-2 h-4 w-4" />
                  Download as Text File
                </Button>

                <Button
                  onClick={downloadReportAsPDF}
                  disabled
                  className="w-full justify-start bg-transparent"
                  variant="outline"
                >
                  <Download className="mr-2 h-4 w-4" />
                  Download as PDF (Coming Soon)
                </Button>

                <Button onClick={() => window.print()} className="w-full justify-start" variant="outline">
                  <Printer className="mr-2 h-4 w-4" />
                  Print Report
                </Button>
              </div>
            </CardContent>
          </Card>

          <Card>
            <CardHeader>
              <CardTitle>Report Contents</CardTitle>
            </CardHeader>
            <CardContent>
              <ul className="space-y-2 text-sm text-muted-foreground list-disc list-inside">
                <li>Patient demographics and medical history</li>
                <li>Attending physician information</li>
                <li>AI-assisted diagnosis with confidence scores</li>
                <li>Contributing factors and feature importance analysis</li>
                <li>Clinical explanation and reasoning</li>
                <li>Input data and metrics used</li>
                <li>Medical codes (ICD-10 and SNOMED CT)</li>
                <li>Recommendations for next steps</li>
                <li>Critical medical disclaimers and limitations</li>
              </ul>
            </CardContent>
          </Card>
        </div>
      </div>
    </main>
  )
}
